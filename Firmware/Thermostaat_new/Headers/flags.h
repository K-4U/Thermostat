#ifndef FLAGS_H_
#define FLAGS_H_


//Flags for main program
//							  7654321076543210
#define FLAG_CV_ON			0b0000000000000001 // 1
#define FLAG_AWAKE			0b0000000000000010 // 2
#define FLAG_HALFSEC		0b0000000000000100 // 4
#define FLAG_100MS			0b0000000000001000 // 8
#define FLAG_SEC			0b0000000000010000 // 16
#define FLAG_DRAWER			0b0000000000100000 // 32
#define FLAG_BATTERYTOOLOW	0b0000000001000000 // 64
#define FLAG_READTIME		0b0000000010000000 // 128
#define FLAG_AWAYSCREEN		0b0000000100000000 // 256
#define FLAG_AWAY			0b0000001000000000 // 512
#define FLAG_WAKE			0b0000010000000000 // 1024
#define CHECKFLAG(flag) (flags & flag)
#define SETFLAG(flag)	(flags |= flag)
#define RESETFLAG(flag)	(flags &= ~flag)

//Flags for drawing stuff. Stuff that should be drawn that is
//							  7654321076543210

#define DRAW_CV_ON			0b0000000000000001 // 1
#define DRAW_DRAWER			0b0000000000000010 // 2
#define DRAW_BATTERY_LOW	0b0000000000000100 // 4
#define DRAW_BATTERY_BAR	0b0000000000001000 // 8
#define DRAW_TTL_BAR		0b0000000000010000 // 16
#define DRAW_TIME			0b0000000000100000 // 32
#define DRAW_DATE			0b0000000001000000 // 64
#define DRAW_AWAYSCREEN		0b0000000010000000 // 128
#define DRAW_SET			0b0000000100000000 // 256
#define DRAW_BUTTONS		0b0000001000000000 // 512
#define DRAW_PRESET			0b0000010000000000 // 1024
#define DRAW_INTERN			0b0000100000000000 // 2048
#define DRAW_EXTERN			0b0001000000000000 // 4096
#define DRAW_CLEAR			0b0010000000000000 // 8192
#define DRAW_CV_OFF			0b0100000000000000 // 16384

#define DRAW_ALL			(DRAW_BATTERY_BAR | DRAW_TTL_BAR | DRAW_TIME | DRAW_DATE | DRAW_SET | DRAW_BUTTONS | DRAW_PRESET | DRAW_INTERN | DRAW_EXTERN)
#define CHECKDRAWFLAG(flag) (drawFlags & flag)
#define SETDRAWFLAG(flag)	(drawFlags |= flag)
#define RESETDRAWFLAG(flag)	(drawFlags &= ~flag)

#define DIFF_X	0b00000001
#define DIFF_Y	0b00000010
#define DIFF_P	0b00000100




#endif /* FLAGS_H_ */